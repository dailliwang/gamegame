<template>
  <div class="open-area">
    <scroll class="arealist-wrapper"
            :data="allList"
            v-if="allList.length"
            :pulldown="pulldown"
            :probe-type="probeType"
            @scrollNearTop="refresh()"
    >
      <div class="arealist">
        <div class="refresh">下拉刷新</div>
        <div class="today">
          <div class="timelist" v-for="item in todayList">
            <h1 class="opentime">{{item.openingtime}}</h1>
            <ul class="openlist">
              <li class="opengame" v-for="game in item.openlist" @click="toDetail(game)">
                <div class="top-wrapper">
                  <div class="pic">
                    <img width="60" height="60" :src="game.ioc_path" :onerror="defaultImg">
                  </div>
                  <div class="desc">
                    <div class="name">{{game.gamename}}</div>
                    <div class="newarea">新服</div>
                  </div>
                </div>
                <div class="areaname">{{game.areaname}}</div>
              </li>
            </ul>
          </div>
        </div>
        <grey-bar></grey-bar>
        <div class="tomorrow">
          <div class="title">明日开服预告</div>
          <div class="timelist" v-for="item in tomorrowList">
            <h1 class="opentime">{{item.openingtime}}</h1>
            <ul class="openlist">
              <li class="opengame" v-for="game in item.openlist" @click="toDetail(game)">
                <div class="top-wrapper">
                  <div class="pic">
                    <img width="60" height="60" :src="game.ioc_path" :onerror="defaultImg">
                  </div>
                  <div class="desc">
                    <div class="name">{{game.gamename}}</div>
                    <div class="newarea">新服</div>
                  </div>
                </div>
                <div class="areaname">{{game.areaname}}</div>
              </li>
            </ul>
          </div>
        </div>
      </div>
      <div class="loading-container" v-show="showLoading">
        <loading></loading>
      </div>
    </scroll>
    <warning ref="warning" @refresh="refresh"></warning>
    <top-tip ref="toptip">
      <div class="tip-title">
        <i class="icon-ok"></i>
        <span class="text">刷新成功</span>
      </div>
    </top-tip>
    <router-view></router-view>
  </div>
</template>

<script>
  import scroll from 'base/scroll/scroll'
  import topTip from 'base/top-tip/top-tip'
  import greyBar from 'base/grey-bar/grey-bar'
  import loading from 'base/loading/loading'
  import warning from 'base/warning/warning'
  import {getOpenAreaList} from 'api/open'
  import {normalizeImage2} from 'common/js/game-img'

  export default {
    data () {
      return {
        todayList:[],
        tomorrowList:[],
        pulldown:true,
        probeType:3,
        showLoading:true,
        defaultImg: 'this.src='+'"static/img/error.png"'
      }
    },
    computed:{
      allList(){
        let arr = this.todayList.concat(this.tomorrowList);
        return arr
      }
    },
    mounted(){
      this.initData();
    },
    methods:{
      initData(refresh){
        this.showLoading = true;
        getOpenAreaList().then((res)=>{
          console.log(res)
          this.showLoading = false;
          this.todayList= normalizeImage2(res.today)
          this.tomorrowList = normalizeImage2(res.tomorrow)
          if(refresh){
            setTimeout(()=>{
              this.$refs.toptip.show();
            },500)
          }
        }).catch((err)=>{
          this.$refs.warning.show();
          this.showLoading = false
        })
      },
      toDetail(item){
        this.$router.push({path:'/openarea/'+item.gameid})
      },
      refresh(){
        this.initData(true)
      }
    },
    beforeRouteEnter(to, from, next){
    	next(true)
    	window.document.location = "js://webview?network=1"
    },
    components:{
      scroll,
      greyBar,
      topTip,
      loading,
      warning
    }
  }
</script>

